/**
  * @brief  This function handles SysTick Handler.
  * @param  None
  * @retval None
  */
void SysTick_Handler(void)
{
	/* General purpose counter */
	if(uSysTick)
		uSysTick++;

	/* Debounce timer */
	if(uKeyDebouncer)
		uKeyDebouncer--;
}

